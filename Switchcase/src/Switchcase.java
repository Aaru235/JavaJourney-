import java.util.*;
class Switchcase
{
	public static void main(String args[])
	{
		Scanner sc = new Scanner(System.in);
		System.out.print("Enter:");
		int n= sc.nextInt();
		switch(n)
		{
		case 1:
			System.out.println("Mon");
		break;
		
		case 2:
			System.out.println("Tue");
		break;
		
		case 3:
			System.out.println("Wed");
		break;
		}
	}
}
